/// Holds pre-loaded fallback certificate and an ACME cert cache for the SNI callback.
struct SniFallback {
    /// Fallback self-signed cert (X509*) — used when no ACME cert is available.
    fallback_x509: *mut std::ffi::c_void,
    /// Fallback private key (EVP_PKEY*)
    fallback_pkey: *mut std::ffi::c_void,
    /// Cache of parsed pointers: hostname → (x509_ptr, pkey_ptr)
    ptr_cache: std::sync::RwLock<HashMap<String, (*mut std::ffi::c_void, *mut std::ffi::c_void)>>,
}

unsafe impl Send for SniFallback {}
unsafe impl Sync for SniFallback {}

/// Ensure a self-signed certificate exists on disk for the given domain,
/// returning (cert_path, key_path).
fn ensure_self_signed_cert(domain: &str, cache_dir: &str) -> Result<(String, String), Box<dyn std::error::Error>> {
    let cert_dir = format!("{}/{}", cache_dir, domain);
    let cert_path = format!("{}/fullchain.pem", cert_dir);
    let key_path = format!("{}/privkey.pem", cert_dir);

    if !std::path::Path::new(&cert_path).exists() || !std::path::Path::new(&key_path).exists() {
        std::fs::create_dir_all(&cert_dir)?;
        lsb_openssl::certs::generate_self_signed(domain, &cert_path, &key_path)?;
    }
    Ok((cert_path, key_path))
}

/// Load certificates from PEM files into raw OpenSSL pointers.
/// Returns (x509_ptr, pkey_ptr) — callers must free with X509_free/EVP_PKEY_free.
fn load_self_signed_ptrs(
    cert_path: &str,
    key_path: &str,
) -> Result<(*mut std::ffi::c_void, *mut std::ffi::c_void), Box<dyn std::error::Error>> {
    let cert_pem = std::fs::read_to_string(cert_path)?;
    let key_pem = std::fs::read_to_string(key_path)?;
    let x509 = lsb_openssl::certs::pem_read_certificate(&cert_pem)
        .map_err(|e| format!("pem_read_certificate: {}", e))?;
    let pkey = lsb_openssl::certs::pem_read_private_key(&key_pem)
        .map_err(|e| format!("pem_read_private_key: {}", e))?;
    Ok((x509, pkey))
}

unsafe extern "C" fn sni_callback(
    ssl: *mut std::ffi::c_void,
    _al: *mut std::os::raw::c_int,
    arg: *mut std::ffi::c_void,
) -> std::os::raw::c_int {
    let state = &*(arg as *const SniFallback);
    let openssl = match crate::openssl_global() {
        Some(o) => o,
        None => return lsb_openssl::SSL_TLSEXT_ERR_ALERT_FATAL,
    };

    // Try to find a cached ACME cert for the requested hostname
    let hostname = openssl.ssl_get_servername(ssl);
    if let Some(ref hostname) = hostname {
        if let Ok(ptr_cache) = state.ptr_cache.read() {
            if let Some(&(x509, pkey)) = ptr_cache.get(hostname) {
                let _ = openssl.ssl_use_certificate(ssl, x509);
                let _ = openssl.ssl_use_private_key(ssl, pkey);
                return lsb_openssl::SSL_TLSEXT_ERR_OK;
            }
        }
    }

    // Fallback: apply the self-signed cert
    if openssl.ssl_use_certificate(ssl, state.fallback_x509).is_err() {
        return lsb_openssl::SSL_TLSEXT_ERR_ALERT_FATAL;
    }
    if openssl.ssl_use_private_key(ssl, state.fallback_pkey).is_err() {
        return lsb_openssl::SSL_TLSEXT_ERR_ALERT_FATAL;
    }
    lsb_openssl::SSL_TLSEXT_ERR_OK
}

/// Get or initialize the global Openssl singleton.
fn openssl_global() -> Option<&'static lsb_openssl::Openssl> {
    static SSL: OnceLock<Option<lsb_openssl::Openssl>> = OnceLock::new();
    SSL.get_or_init(|| {
        let ssl = lsb_openssl::Openssl::load().ok()?;
        ssl.init().ok()?;
        Some(ssl)
    }).as_ref()
}

